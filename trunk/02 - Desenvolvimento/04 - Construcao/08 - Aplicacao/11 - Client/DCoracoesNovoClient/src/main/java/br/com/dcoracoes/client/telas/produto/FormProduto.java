    @Override
    public void initialize() {

        createNew();

        //evento de inserção de tados na grid de cor
        tableInfCores.getModel().addTableModelListener(new TableModelListener() {

            @Override
            public void tableChanged(TableModelEvent e) {

                DefaultTableModel dtm = (DefaultTableModel) tableInfCores.getModel();
                if (tableInfCores.getSelectedRow() >= 0) {
                    if (dtm.getRowCount() > 1) {
                        for (int linha = 0; linha < dtm.getRowCount(); linha++) {
                            if (dtm.getValueAt(linha, 0) != null) {
                                if ((dtm.getValueAt(linha, 0).equals(dtm.getValueAt(tableInfCores.getSelectedRow(), 0)))
                                        && (linha != tableInfCores.getSelectedRow())) {

                                    dtm.setValueAt(null, tableInfCores.getSelectedRow(), 0);
                                    selectTableCell(tableInfCores.getSelectedRow(), 0);
                                    JOptionPane.showMessageDialog(null, MessageProduto.CODIGO_COR_REPETIDO, MensagensUtil.ATENCAO, 2);
                                    return;
                                }
                            }
                        }
                    }

                    if (e.getColumn() == 2 || e.getColumn() == 3) {
                        int row = tableInfCores.getSelectedRow();
                        if ((Integer) dtm.getValueAt(row, 3) <= (Integer) dtm.getValueAt(row, 2)
                                && (Integer) dtm.getValueAt(row, 3) != 0
                                && (Integer) dtm.getValueAt(row, 2) != 0) {

                            dtm.setValueAt(MessageProduto.ESTOQUE_BAIXO, row, 4);

                        } else {
                            dtm.setValueAt("", row, 4);
                        }
                    }
                }


                //incrementa total de produtos     
                int countTotal = 0;
                for (int i = 0; i < dtm.getRowCount(); i++) {

                    countTotal += ((Integer) dtm.getValueAt(i, 3)).intValue();

                }

                txtEstoqueTotal.setText(countTotal + "");
            }
        });

        //pular de campo com enter
        MetodosUtil.enterPularCampos(this);
        //define letras maiusculas
        MetodosUtil.letrasMaiusculas(this.getContentPane());

        //Logica para fechar tela pela botao do frame
        addListenerParaExibirMgsFecharTela();

        //Inicializa Acesso
        this.inicializaAcessoPorPerfil();
    }

