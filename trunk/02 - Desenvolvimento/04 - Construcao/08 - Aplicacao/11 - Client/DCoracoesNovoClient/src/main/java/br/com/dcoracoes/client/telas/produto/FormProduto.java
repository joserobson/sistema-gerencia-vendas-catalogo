    /**
     * popula a grid
     *
     * @param aux
     */
    private void popularGrid(ItemProduto aux) {
        if (aux.getCor() == null) {
            aux.setCor(new Cor());
        }

        DefaultTableModel dtm = (DefaultTableModel) tableInfCores.getModel();

        String alerta = null;
        if (aux.getEstoqueAtual() <= aux.getEstoqueMinimo()
                && aux.getEstoqueAtual() != 0
                && aux.getEstoqueMinimo() != 0) {
            alerta = MessageProduto.ESTOQUE_BAIXO;
        }

        dtm.addRow(new Object[]{
                    aux.getCor().getCodigo(),
                    aux.getCor().getDescricao(),
                    aux.getEstoqueMinimo(),
                    aux.getEstoqueAtual(),
                    alerta
                });
                
    }

    private StringBuilder validarCor() {
        DefaultTableModel dtm = (DefaultTableModel) tableInfCores.getModel();
        if (dtm.getRowCount() == 0) {
            return new StringBuilder();
        }

        int indexUltimaCor = dtm.getRowCount() - 1;

        StringBuilder stringError = new StringBuilder();
        if (dtm.getValueAt(indexUltimaCor, 0) == null || dtm.getValueAt(indexUltimaCor, 0).toString().isEmpty()) {
            stringError.append(MessageProduto.COLUNA_COR_OBRIGATORIO);
        }

        if (dtm.getValueAt(indexUltimaCor, 1) == null || dtm.getValueAt(indexUltimaCor, 1).toString().isEmpty()) {
            if (!stringError.toString().isEmpty()) {
                stringError.append("\n");
            }
            stringError.append(MessageProduto.COLUNA_DESCRICAO_COR_OBRIGATORIO);
        }

        return stringError;
    }

