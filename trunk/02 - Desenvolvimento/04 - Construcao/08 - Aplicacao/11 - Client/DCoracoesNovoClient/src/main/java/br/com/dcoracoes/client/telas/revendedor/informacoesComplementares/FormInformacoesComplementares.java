    /**
     * valida o endereco passado verificando se algo foi preenchido
     * @param end
     * @return 
     */
    private boolean validEndereco(Endereco end) {
        if (!end.getRua().isEmpty()
                || !end.getNumero().isEmpty()
                || !end.getComplemento().isEmpty()
                || !end.getCidade().isEmpty()
                || end.getEstado() != null
                || end.getCep() != null) {
            return true;
        }

        return false;
    }

    public boolean pushToModelBoolean() {
        pushToModelConjugue();
        pushToModelConta1();
        pushToModelConta2();
        pushToModelConta3();
        if (lstInformacoesComplementares == null) {
            lstInformacoesComplementares = new ArrayList<ViewInfComplementaresRevendedor>();
        } else {
            lstInformacoesComplementares.clear();
        }
        try {
            if (conta1 != null) {
                if (validContaPreenchida(conta1)) {
                    lstInformacoesComplementares.add(conta1);
                }
            }

            if (conta2 != null) {
                if (validContaPreenchida(conta2)) {
                    lstInformacoesComplementares.add(conta2);
                }
            }

            if (conta3 != null) {
                if (validContaPreenchida(conta3)) {
                    lstInformacoesComplementares.add(conta3);
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), MensagensUtil.ATENCAO, 2);
            return false;
        }
        return true;
    }

    @Override
    public void salvar() {
        form.getViewRevendedor().getLstInformacoesComplementares().clear();
        if (pushToModelBoolean()) {
            this.dispose();
            form.getViewRevendedor().getLstInformacoesComplementares().addAll(getInformacoesComplementares());
            form.getViewRevendedor().setConjugue(getConjugue());
            if(form.getViewRevendedor().getRevendedor().getId() != null){
                try {
                    form.mantemRevendedor(form.getViewRevendedor());
                } catch (Exception ex) {
                    Logger.getLogger(FormInformacoesComplementares.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }

    @Override
    public void close() {
        if (!btnSalvar.isEnabled()) {
            this.dispose();
        } else {
            int opcao = JOptionPane.showOptionDialog(rootPane, MensagensUtil.MENSAGEM_CONFIRMAR_SAIR, MensagensUtil.ATENCAO,  
                JOptionPane.YES_NO_OPTION,  
                JOptionPane.QUESTION_MESSAGE, null, new String[]{"Sim", "Não"}, "Não"); 
            if (opcao == JOptionPane.YES_OPTION) {
                this.dispose();
                form.setFormInformacoesComplementares(new FormInformacoesComplementares(form));
                form.getFormInformacoesComplementares().editar(true);

                form.getFormInformacoesComplementares().setInformacoesComplementares(form.getViewRevendedor().getLstInformacoesComplementares());
                form.getFormInformacoesComplementares().setConjugue(form.getViewRevendedor().getConjugue());
                form.getFormInformacoesComplementares().popularTela();
            }
        }
    }

