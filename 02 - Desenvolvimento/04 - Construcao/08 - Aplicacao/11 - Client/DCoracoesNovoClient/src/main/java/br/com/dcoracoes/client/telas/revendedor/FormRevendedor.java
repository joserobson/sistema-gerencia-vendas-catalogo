    private void jtxtLimiteCreditoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jtxtLimiteCreditoFocusLost
        // TODO add your handling code here:
        jtxtLimiteUtilizavel.requestFocus();
        txtAreaObservacao.requestFocus();

        if (this.viewRevendedor.getRevendedor() == null) {
            jtxtLimiteUtilizavel.setText(jtxtLimiteCredito.getText());
        } else {
            float lim = Float.parseFloat(jtxtLimiteCredito.getText().replace(".", "").replace(",", "."))
                    - this.viewRevendedor.getRevendedor().getLimiteEmUso();
            jtxtLimiteUtilizavel.setText(MetodosUtil.formatarValorDinheiro(String.valueOf(lim)));
        }
    }//GEN-LAST:event_jtxtLimiteCreditoFocusLost

