    /**
     * recupera os dados dá aba dados pricipais
     */
    private void pushToModelDadosPrincipais() {

        PessoaFisica pessoa = (PessoaFisica) this.viewRevendedor.getRevendedor().getPessoa();
        if (pessoa == null) {
            pessoa = new PessoaFisica();
            this.viewRevendedor.getRevendedor().setPessoa(pessoa);
        }

        pessoa.setNome(txtRevendedor.getText());
        if (!jtxtCPF.getText().replace(".", "").replace("-", "").trim().isEmpty()) {
            pessoa.setCpf(jtxtCPF.getText());
        } else
            pessoa.setCpf(null);
            
        pessoa.setIdentidade(jtxtRG.getText());

        if (!jtxtDataNascimento.getText().replace("/", "").trim().isEmpty()) {
            String[] dateAux = jtxtDataNascimento.getText().split("/");
            GregorianCalendar c = new GregorianCalendar();
            c.set(Integer.parseInt(dateAux[2]), Integer.parseInt(dateAux[1]) - 1, Integer.parseInt(dateAux[0]));
            pessoa.setDataNascimento(DateUtil.dateAsXMLGregorianCalendar(c.getTime()));
        } else
            pessoa.setDataNascimento(null);

        if(returnEnumEstadoCivil() == null)
            pessoa.setEstadoCivil(0);
        else
            pessoa.setEstadoCivil(returnEnumEstadoCivil().getCodigo());

        pessoa.setEndereco(pushToModelEndereco(pessoa));

    }

    /**
     * Retorna o Estado Civil selecionado na combobox
     * @return 
     */
    private Enum_EstadoCivil returnEnumEstadoCivil() {
        if (cbEstadoCivil.getSelectedIndex() == 1) {
            return Enum_EstadoCivil.CASADO;
        } else if (cbEstadoCivil.getSelectedIndex() == 2) {
            return Enum_EstadoCivil.SOLTEIRO;
        } else if (cbEstadoCivil.getSelectedIndex() == 3) {
            return Enum_EstadoCivil.SEPARADO;
        } else if (cbEstadoCivil.getSelectedIndex() == 4) {
            return Enum_EstadoCivil.DIVORCIADO;
        } else if (cbEstadoCivil.getSelectedIndex() == 5) {
            return Enum_EstadoCivil.UNIAOESTAVEL;
        } else
            return null;
    }

    /**
     * Recupera os dados de endereço 
     * @return 
     */
    private Endereco pushToModelEndereco(Pessoa pessoa) {
        Endereco end = pessoa.getEndereco();
        if (end == null) {
            end = new Endereco();
        }
        end.setRua(txtEndereco.getText());
        end.setNumero(txtNumero.getText());
        end.setComplemento(txtComplemento.getText());
        end.setCidade(txtCidade.getText());
        end.setEstado(cbUF.getSelectedItem().toString());
        if (!jtxtCEP.getText().replace("-", "").trim().isEmpty()) {
            end.setCep(jtxtCEP.getText());
        } else
            end.setCep(null);
        end.setBairro(txtBairro.getText());
        end.setReferencia(txtReferencia.getText());

        return end;
    }

    /**
     * Recupera os dados da aba contato
     */
    private void pushToModelContato() {
        PessoaFisica pessoa = (PessoaFisica) this.viewRevendedor.getRevendedor().getPessoa();
        pessoa.getTelefones().addAll(returnListTelefones(pessoa));
        pessoa.getEmails().addAll(returnListEmails(pessoa));
    }

    private Telefone buscaTelefonePorTipo(List<Telefone> list, int tipoTelefone, boolean celValid) {
        if (list != null
                && list.size() > 0) {
            for (Telefone telefone : list) {
                if (telefone.getTipoTelefone() == tipoTelefone) {
                    if (celValid) {
                        celValid = false;
                        continue;
                    }
                    return telefone;
                }
            }
        }

        return new Telefone();
    }

    /**
     * Mosta a lista de telefones fornecidos na tela
     * @return 
     */
    private List<Telefone> returnListTelefones(Pessoa pessoa) {
        List<Telefone> list = new ArrayList<Telefone>();
        Telefone tel = null;

        //telefone Residencial
        if (!jtxtTelefoneResidencial.getText().replace("-", "").trim().isEmpty()) {
            tel = buscaTelefonePorTipo(pessoa.getTelefones(), Enum_TipoTelefone.RESIDENCIAL.getCodigo(), false);
            tel.setNumero(jtxtTelefoneResidencial.getText());
            tel.setOperadora((cbOperadora1.getSelectedItem().toString().equals("selecione")) ? null : cbOperadora1.getSelectedItem().toString());
            tel.setTipoTelefone(Enum_TipoTelefone.RESIDENCIAL.getCodigo());
            list.add(tel);
        }

        //telefone Celular 1
        if (!jtxtCelular1.getText().replace("-", "").trim().isEmpty()) {
            tel = buscaTelefonePorTipo(pessoa.getTelefones(), Enum_TipoTelefone.CELULAR.getCodigo(), false);
            tel.setNumero(jtxtCelular1.getText());
            tel.setOperadora((cbOperadora1.getSelectedItem().toString().equals("selecione")) ? null : cbOperadora1.getSelectedItem().toString());
            tel.setTipoTelefone(Enum_TipoTelefone.CELULAR.getCodigo());
            list.add(tel);
        }

        //telefone Comercial
        if (!jtxtTelefoneComercial.getText().replace("-", "").trim().isEmpty()) {
            tel = buscaTelefonePorTipo(pessoa.getTelefones(), Enum_TipoTelefone.COMERCIAL.getCodigo(), false);
            tel.setNumero(jtxtTelefoneComercial.getText());
            tel.setOperadora((cbOperadora2.getSelectedItem().toString().equals("selecione")) ? null : cbOperadora2.getSelectedItem().toString());
            tel.setTipoTelefone(Enum_TipoTelefone.COMERCIAL.getCodigo());
            list.add(tel);
        }


        //telefone Celular 2
        if (!jtxtCelular2.getText().replace("-", "").trim().isEmpty()) {
            tel = buscaTelefonePorTipo(pessoa.getTelefones(), Enum_TipoTelefone.CELULAR.getCodigo(), true);
            tel.setNumero(jtxtCelular2.getText());
            tel.setOperadora((cbOperadora2.getSelectedItem().toString().equals("selecione")) ? null : cbOperadora2.getSelectedItem().toString());
            tel.setTipoTelefone(Enum_TipoTelefone.CELULAR.getCodigo());
            list.add(tel);
        }

        return list;
    }

    private Email buscaEmail(List<Email> list, boolean segundoEmail) {
        if (list != null
                && list.size() > 0) {
            for (Email email : list) {
                if (segundoEmail) {
                    segundoEmail = false;
                    continue;
                }
                return email;

            }
        }

        return new Email();
    }

    /**
     * Monta a lista de email fornecidos na tela
     * @return 
     */
    private List<Email> returnListEmails(Pessoa pessoa) {
        List<Email> list = new ArrayList<Email>();
        Email email = null;
        if (!jtxtEmail1.getText().isEmpty()) {
            email = buscaEmail(pessoa.getEmails(), false);
            email.setEnderecoEmail(jtxtEmail1.getText());
            list.add(email);
        }
        if (!jtxtEmail2.getText().isEmpty()) {
            email = buscaEmail(pessoa.getEmails(), true);
            email.setEnderecoEmail(jtxtEmail2.getText());
            list.add(email);
        }

        return list;
    }

