    /**
     * gera o protocolo para a prospecção
     * @return 
     */
    private String geraProtocolo() {
        String protocolo = null;
        try {
            AlertaServerImpl alertaServerImpl = new AlertaServerImpl();

            protocolo = alertaServerImpl.gerarNumeroProtocolo();
        } catch (Exception ex) {
            LogUtil.logDescricaoErro(this.getClass(), ex);
            Logger.getLogger(FormProspeccoes.class.getName()).log(Level.SEVERE, null, ex);
        }

        return protocolo;
    }

