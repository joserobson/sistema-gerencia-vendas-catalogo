    @Override
    public boolean validarTela() {
        StringBuilder stringError = new StringBuilder();
        if (txtRevendedor.getText().trim().isEmpty()) {
            stringError.append(MessageProspeccao.CAMPO_CONSULTOR_OBRIGATORIO);
        }
        if (txtAssunto.getText().trim().isEmpty()) {
            if (!stringError.toString().isEmpty()) {
                stringError.append("\n");
            }
            stringError.append(MessageProspeccao.CAMPO_ASSUNTO_OBRIGATORIO);
        }
        if (cbSituacao.getSelectedIndex() == 0) {
            if (!stringError.toString().isEmpty()) {
                stringError.append("\n");
            }
            stringError.append(MessageProspeccao.CAMPO_SITUACAO_OBRIGATORIO);
        }

        if (!stringError.toString().isEmpty()) {
            JOptionPane.showMessageDialog(this, stringError, MensagensUtil.ATENCAO, 2);
        }

        return stringError.toString().isEmpty();
    }

    @Override
    public void pushToModel() {
        if (prospeccao == null) {
            prospeccao = new Alerta();
        }

        prospeccao.setDataAlerta(DateUtil.dateAsXMLGregorianCalendar(dateCriacaoAlerta));

        prospeccao.setAtendente(App.getUserLog());

        if (revendedor == null) {
            if (prospeccao.getPessoa() == null) {
                prospeccao.setPessoa(new Pessoa());
            }
            prospeccao.getPessoa().setNome(txtRevendedor.getText().trim());
        } else {
            prospeccao.setPessoa(revendedor.getPessoa());
        }

        prospeccao.setProtocolo(txtProtocolo.getText().trim());

        prospeccao.setAssunto(txtAssunto.getText().trim());

        prospeccao.setDescricao(txtAreaDescricao.getText().trim());

        if (!cbSituacao.getSelectedItem().toString().contains(MessageProspeccao.OPCAO_SELECIONE)) {
            prospeccao.setSituacaoAlerta(recValorSituacao(cbSituacao.getSelectedItem().toString()));
        }

    }

